export interface IJobHeader {
  /**
   * The title of the job.
   * (Optional property)
   */
  jobTitle?: string
  /**
   * The subtitle or additional information about the job.
   * (Optional property)
   */
  jobSubtitle?: string
  /**
   * Specifies whether the job header is hidden or visible.
   * (Optional property)
   */
  isHidden?: boolean
}

export interface IDivider {
  /**
   * You can change the width with values from 0 to 100
   */
  width?: number
  /**
   * You can change the divider color
   */
  color?: string
  /**
   * You can change the opacity with values from 0 to 10
   */
  opacity?: number
  /**
   * You can change the vertical spacing
   */
  spacing?: number
  className?: string
}
